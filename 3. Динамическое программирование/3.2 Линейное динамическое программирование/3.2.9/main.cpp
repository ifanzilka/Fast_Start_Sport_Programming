#include <iostream>

using namespace std;

int main()
{
    unsigned long long n, f1=1, f2=1, f3;
    cin >> n;
    for (long i = 0; i != n*2; ++i)
    {
        f3 = f1 + f2;
        f1 = f2;
        f2 = f3;
    }
    cout << f2;
    return 0;
}
